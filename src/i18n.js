let currentLanguage = localStorage.getItem(LANG_STORAGE_KEY) || "it";

export function language() {
  return currentLanguage;
}

export function setLanguage(nextLanguage) {
  currentLanguage = dictionaries[nextLanguage] ? nextLanguage : "it";
  localStorage.setItem(LANG_STORAGE_KEY, currentLanguage);
  document.documentElement.lang = currentLanguage;
}

export function t(key, params = {}) {
  const value = key.split(".").reduce((node, part) => node?.[part], dictionaries[currentLanguage])
    ?? key.split(".").reduce((node, part) => node?.[part], dictionaries.it)
    ?? key;
  return Object.entries(params).reduce((text, [name, replacement]) => text.replaceAll(`{${name}}`, replacement), value);
}

export function applyTranslations(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.title = t(node.dataset.i18nTitle);
  });
  root.querySelectorAll("[data-i18n-aria]").forEach((node) => {
    node.setAttribute("aria-label", t(node.dataset.i18nAria));
  });
}
